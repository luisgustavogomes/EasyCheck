package br.edu.qi.util;

/**
 *
 * @author Anderson Jorge
 */
public class Espelho {

    private String data;
    private String hora;
    private String tipo;

    public Espelho(String data, String hora, String tipo) {
        this.data = data;
        this.hora = hora;
        this.tipo = tipo;
    }

    public String getData() {
        return data;
    }

    public void setData(String data) {
        this.data = data;
    }

    public String getHora() {
        return hora;
    }

    public void setHora(String hora) {
        this.hora = hora;
    }

    public String getTipo() {
        return tipo;
    }

    public void setTipo(String tipo) {
        this.tipo = tipo;
    }

}
